function User(id, account, fullname, password, email, user, language, about, avatar) {
  this.id = id;
  this.account = account;
  this.fullname = fullname;
  this.password = password;
  this.email = email;
  this.user = user;
  this.language = language;
  this.about = about;
  this.avatar = avatar;
}
